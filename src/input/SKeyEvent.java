	private char keyChar;
	private int keyCode;
	private long time;
	
	//x & y pos?
	
	public SKeyEvent(char keyChar, int keyCode) {
		time = System.currentTimeMillis();
		this.keyChar = keyChar;
		this.keyCode = keyCode;
	}
	
	public char getKeyChar() {
		return keyChar;
	}
	
	public int getKeyCode() {
		return keyCode;
	}
	
	public long getTime() {
		return time;
	}
	
	@Override
	public String toString() {
		return "KeyEvent{keyChar=" + keyChar + " keyCode=" + keyCode  + " time=" + time + "}";
	}
